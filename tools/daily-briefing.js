const fs = require('fs');
const path = require('path');

function generateBriefing(patient, tasks, appointments, notifications) {
  const p = patient || {};
  const today = new Date().toLocaleDateString('en-US',{weekday:'long',month:'long',day:'numeric'});
  const todayStr = new Date().toISOString().split('T')[0];
  const dueTasks = (tasks||[]).filter(t => t.status !== 'done' && (t.priority === 'high' || t.dueDate === todayStr));
  const upcomingAppts = (appointments||[]).filter(a => {
    if(!a.date) return false;
    const diff = (new Date(a.date) - new Date()) / (1000*60*60*24);
    return diff >= 0 && diff <= 7;
  }).slice(0,3);
  const meds = (p.medications||[]);
  const unreadNotifs = (notifications||[]).filter(n => !n.read).slice(0,3);
  const sections = [];
  sections.push(`Good morning! Here is Linda's health briefing for ${today}.`);
  if(meds.length) {
    const morning = meds.filter(m => {
      const f = (m.frequency||'').toLowerCase();
      return f.includes('daily') || f.includes('morning') || f.includes('once');
    });
    if(morning.length) sections.push(`**Morning Medications:** ${morning.map(m => m.name+' '+(m.dose||'')).join(', ')}`);
  }
  if(upcomingAppts.length) {
    const lines = upcomingAppts.map(a => {
      const d = new Date(a.date).toLocaleDateString('en-US',{weekday:'short',month:'short',day:'numeric'});
      return `${d} - ${a.doctorName||a.title||'Appointment'}${a.location?' at '+a.location:''}`;
    });
    sections.push(`**Upcoming Appointments:**\n${lines.map(l=>'- '+l).join('\n')}`);
  } else {
    sections.push(`**Appointments:** No appointments in the next 7 days.`);
  }
  if(dueTasks.length) sections.push(`**Today's Tasks:** ${dueTasks.map(t=>t.title).join(', ')}`);
  else sections.push(`**Tasks:** All clear - no urgent tasks today.`);
  if(unreadNotifs.length) sections.push(`**Alerts:** ${unreadNotifs.map(n=>n.title).join(' | ')}`);
  const conditions = (p.conditions||[]).map(c=>c.toLowerCase());
  const tips = [];
  if(conditions.some(c=>c.includes('thyroid'))) tips.push('Synthroid should be taken on empty stomach 30-60 min before food.');
  if(conditions.some(c=>c.includes('diabetes'))) tips.push('Check blood sugar if not done yet today.');
  if(conditions.some(c=>c.includes('hypertension'))) tips.push('Consider recording a blood pressure reading this morning.');
  if(tips.length) sections.push(`**Health Reminder:** ${tips[0]}`);
  return {
    date: todayStr,
    patientName: p.name || 'Patient',
    text: sections.join('\n\n'),
    sections: { medications: meds, appointments: upcomingAppts, tasks: dueTasks, alerts: unreadNotifs }
  };
}

function generateHTML(briefing) {
  const lines = briefing.text.split('\n\n').map(s => {
    s = s.replace(/\*\*(.+?)\*\*/g,'<strong>$1</strong>').replace(/\n/g,'<br>');
    return `<p style="margin:0 0 14px;font-size:14px;color:#e2e8f0;line-height:1.6">${s}</p>`;
  }).join('');
  return `<!DOCTYPE html><html><head><meta charset="UTF-8"><style>body{font-family:-apple-system,sans-serif;background:#0f172a;color:#f1f5f9;padding:24px;max-width:600px;margin:0 auto}h1{font-size:20px;color:#2dd4bf;margin-bottom:4px}.sub{font-size:13px;color:#94a3b8;margin-bottom:20px}.card{background:rgba(30,41,59,0.6);border:1px solid rgba(148,163,184,0.12);border-radius:16px;padding:18px}</style></head><body><h1>Health Agent Daily Briefing</h1><div class="sub">For ${briefing.patientName} - ${new Date(briefing.date).toLocaleDateString('en-US',{weekday:'long',month:'long',day:'numeric'})}</div><div class="card">${lines}</div><p style="font-size:11px;color:#475569;margin-top:20px">Generated by Health Agent</p></body></html>`;
}

module.exports = { generateBriefing, generateHTML };
